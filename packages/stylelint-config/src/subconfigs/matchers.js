/**
 * Identifiers cases matchers
 */

export const pascalCaseMatcher = /^([A-Z][a-z\d]*)+$/

export const camelCaseMatcher = /^[a-z\d]+([A-Z][a-z\d]*)*$/

export const strictMediaQueryMatcher = /^media([A-Z][a-z\d]*)+$/

export const strictCustomPropertyMatcher = /^(size|color|duration|shadow|opacity|zIndex|font|animation)([A-Z][a-z\d]*)+$/
